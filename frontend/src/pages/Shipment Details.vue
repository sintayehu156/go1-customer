<script>
import LeftSidebar from '@/components/Custom Layout/LeftSidebar.vue'
import { ref, watch, onMounted, computed } from 'vue'
import { createResource, Breadcrumbs, Button, Badge } from 'frappe-ui'
import { useRouter, useRoute } from 'vue-router'

export default {
  components: {
    LeftSidebar,
    Breadcrumbs,
    Button,
    Badge,
  },
  setup() {
    const router = useRouter()
    const route = useRoute()

    const isSidebarCollapsed = ref(false)
    const name = ref('')
    const addressLine1 = ref([])
    const addressLine2 = ref([])
    const inputValue = ref('')
    const projectValue = ref([])
    const itemValue = ref([])
    const taxValue = ref([])
    const totalValue = ref([])
    const grandValue = ref([])
    const totalTaxValue = ref([])
    const dateValue = ref([])
    const customerName = ref([])
    const city = ref([])
    const country = ref([])
    const phone = ref([])
    const state = ref([])
    const pincode = ref([])
    const inWord = ref([])
    const duedateValue = ref([])
    const quotationTo = ref([])
    const delivery = createResource({
      url: 'go1_customer.go1_customer.api.api.get_shipments',
      method: 'get',
    })

    const breadcrumbsList = ref([
      { label: 'Shipments', route: { name: 'shipments' } },
      { label: '', route: {} },
    ])

    const fetchInvoiceDetails = async () => {
      try {
        const id = route.params.id
        const data = await delivery.fetch()
        console.log('data', data)
        const InvoiceDetails = data.find((item) => item.name === id)

        if (InvoiceDetails) {
          name.value = InvoiceDetails.name
          inputValue.value = InvoiceDetails.status
          itemValue.value = InvoiceDetails.items || []
          projectValue.value = InvoiceDetails.project
          taxValue.value = InvoiceDetails.taxes || []
          grandValue.value = InvoiceDetails.rounded_total
          addressLine1.value = InvoiceDetails.address_line1
          addressLine2.value = InvoiceDetails.address_line2
          city.value = InvoiceDetails.city
          country.value = InvoiceDetails.country
          phone.value = InvoiceDetails.phone
          state.value = InvoiceDetails.state
          pincode.value = InvoiceDetails.pincode
          inWord.value = InvoiceDetails.in_words
          totalValue.value = InvoiceDetails.total
          totalTaxValue.value = InvoiceDetails.total_taxes_and_charges
          quotationTo.value = InvoiceDetails.quotation_to
          customerName.value = InvoiceDetails.customer
          dateValue.value = InvoiceDetails.posting_date
          duedateValue.value = InvoiceDetails.due_date
        }
      } catch (error) {
        console.error('Error fetching delivery details:', error)
      }
    }

    const toggleSidebar = () => {
      isSidebarCollapsed.value = !isSidebarCollapsed.value
    }

    watch(name, (newName) => {
      breadcrumbsList.value[1].label = newName
    })

    onMounted(() => {
      fetchInvoiceDetails()
    })

    const statusColor = computed(() => {
      switch (inputValue.value.toLowerCase()) {
        case 'draft':
          return 'red'
        case 'deliveryed':
          return 'green'
        case 'partially deliveryed':
          return 'yellow'
        case 'lost':
          return 'red'
        case 'cancelled':
          return 'red'
        case 'expired':
          return 'gray'
        default:
          return 'gray'
      }
    })

    const statusColorText = computed(() => {
      switch (inputValue.value.toLowerCase()) {
        case 'draft':
          return 'text-red-700'
        case 'deliveryed':
          return 'text-green-700'
        case 'partially deliveryed':
          return 'text-yellow-700'
        case 'lost':
          return 'text-red-700'
        case 'cancelled':
          return 'text-red-700'
        case 'expired':
          return 'text-gray-700'
        default:
          return 'text-gray-700'
      }
    })

    const statusBorColor = computed(() => {
      switch (inputValue.value.toLowerCase()) {
        case 'draft':
          return 'bdelivery-red-400'
        case 'deliveryed':
          return 'bdelivery-green-400'
        case 'partially deliveryed':
          return 'bdelivery-yellow-400'
        case 'lost':
          return 'bdelivery-red-400'
        case 'cancelled':
          return 'bdelivery-red-600'
        case 'expired':
          return 'bdelivery-gray-600'
        default:
          return 'bdelivery-gray-300'
      }
    })

    
    const bdeliveryWidth = computed(() => 'auto')

    return {
      isSidebarCollapsed,
      name,
      inWord,
      inputValue,
      addressLine1,
      itemValue,
      taxValue,
      customerName,
      totalValue,
      dateValue,
      quotationTo,
      totalTaxValue,
      grandValue,
      toggleSidebar,
      projectValue,
      breadcrumbsList,
      duedateValue,
      statusColor,
      statusColorText,
      statusBorColor,
      bdeliveryWidth,
      addressLine2,
      city,
      country,
      phone,
      state,
      pincode,
    }
  },
}
